    @Test
    @Transactional
    void putExistingEmployee() throws Exception {
        // Initialize the database
        insertedEmployee = employeeRepository.saveAndFlush(employee);

        long databaseSizeBeforeUpdate = getRepositoryCount();

        // Update the employee
        Employee updatedEmployee = employeeRepository.findById(employee.getId()).orElseThrow();
        // Disconnect from session so that the updates on updatedEmployee are not directly saved in db
        em.detach(updatedEmployee);
        updatedEmployee
            .displayName(UPDATED_DISPLAY_NAME)
            .email(UPDATED_EMAIL)
            .title(UPDATED_TITLE)
            .telephoneNumber(UPDATED_TELEPHONE_NUMBER)
            .localPhone(UPDATED_LOCAL_PHONE)
            .department(UPDATED_DEPARTMENT)
            .physicalDeliveryOfficeName(UPDATED_PHYSICAL_DELIVERY_OFFICE_NAME)
            .distinguishedName(UPDATED_DISTINGUISHED_NAME)
            .username(UPDATED_USERNAME);

        restEmployeeMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedEmployee.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(om.writeValueAsBytes(updatedEmployee))
            )
            .andExpect(status().isOk());

        // Validate the Employee in the database
        assertSameRepositoryCount(databaseSizeBeforeUpdate);
        assertPersistedEmployeeToMatchAllProperties(updatedEmployee);
    }

